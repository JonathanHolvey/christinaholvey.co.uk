		<p>Our home lies in the formidable limestone shadow of the Mendips. In winter great flocks of finches pour down from the hills like tiny multicoloured iron-filings to the naked tree branches in our garden, while in summer the beds are a riot of colour with plants we have grown from seed. British birds and flowering plants inspire my art; I love their strength of form and patterns. The Jacob sheep in my paintings have also been friends since childhood. Time spent gardening throws up ideas which are then worked into compositions over a hot coffee and a mug of pencils back in the studio, where the floor boards are painted as green as the surrounding hills.</p>
